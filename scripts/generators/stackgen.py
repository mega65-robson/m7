def generate(isPush,registers,name):    
    global genidx
    rs = [x for x in registers.upper()]
    rs.sort()
    rs = ",".join(rs)
    print(";; {0} registers {1} {2} the stack\n".format("Push" if isPush else "Pop",rs,"on" if isPush else "off"))
    print("\t.bytecode \"{1}\"\nGenStack{0}:".format(genidx,name))
    genidx += 1
    print("\t.stackopen")
    for r in registers:
        if isPush:
            print("\t.rpush {0}Register".format(r))
        else:
            print("\t.rpop {0}Register".format(r))
    print("\t.stackclose")
    print("\t.donext\n")

genidx = 0

def generatePush(registers,name = None):    
    name = registers+">" if name is None else name
    generate(True,registers,name)

def generatePop(registers,name = None):
    name = ">"+registers if name is None else name
    registers = registers[::-1]
    generate(False,registers,name)


def generatePair(registers):
    generatePush(registers)
    generatePop(registers)

print(";;\n;;\tThis file is automatically generated.\n;;")

print("\t.section code")

generatePair("a")
generatePair("b")
generatePair("c")

generatePair("ab")
generatePair("bc")

generatePair("abc")

print("\t.endsection")            
