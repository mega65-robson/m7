        .section code

; ************************************************************************************************
;
;;                                      Negate A
;
; ************************************************************************************************

		.bytecode  "negate"
Negate:        
        sec
        lda     #0
        sbc     aRegister
        sta     aRegister
        lda     #0
        sbc     aRegister+1
        sta     aRegister+1
        rts

; ************************************************************************************************
;
;;                           Set A to the binary Not of A (1's complement)
;
; ************************************************************************************************

		.bytecode  "not"
NotA:
        lda     aRegister
        eor     #$FF
        sta     aRegister
        lda     aRegister+1
        eor     #$FF
        sta     aRegister+1
        rts

; ************************************************************************************************
;
;;                           Set A to the absolute value of A
;
; ************************************************************************************************

		.bytecode  "abs"
AbsA:
        lda     aRegister+1
        bmi     Negate
        rts

; ************************************************************************************************
;
;;                                  Shift A left 8 times
;
; ************************************************************************************************

		.bytecode  "256*"
Times256:
        lda     aRegister
        sta     aRegister+1
        stz     aRegister
        rts

; ************************************************************************************************
;
;;                                  Shift A right 8 times
;
; ************************************************************************************************

		.bytecode  "256/"
Div256:
        lda     aRegister+1
        sta     aRegister
        stz     aRegister+1
        rts

        .endsection

