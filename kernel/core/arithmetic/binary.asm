        .section code

; ************************************************************************************************
;
;;                                       Add B to A
;
; ************************************************************************************************

		.bytecode  "+"
AddBinary:
        clc
        .binop  adc  

; ************************************************************************************************
;
;;                                       Add B to A
;
; ************************************************************************************************

		.bytecode  "-"
SubBinary:
        sec
        .binop  sbc

; ************************************************************************************************
;
;;                                 Binary and A and B to A
;
; ************************************************************************************************

		.bytecode  "and"
AndBinary:
        .binop  and


; ************************************************************************************************
;
;;                                 Binary or A and B to A
;
; ************************************************************************************************

		.bytecode  "or"
OrBinary:
        .binop  ora

; ************************************************************************************************
;
;;                                 Binary xor A and B to A
;
; ************************************************************************************************

		.bytecode  "xor"
XorBinary:
        .binop  eor

        .endsection

