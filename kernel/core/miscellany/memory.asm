        .section code

; ************************************************************************************************
;
;;                                  Read the byte at A into A
;
; ************************************************************************************************

		.bytecode  "c@"
ReadByte:
        lda     (aRegister)
        sta     aRegister
        stz     aRegister+1
        .donext

; ************************************************************************************************
;
;;                                  Read the word at A into A
;
; ************************************************************************************************

		.bytecode  "@"
ReadWord:
        ldy     #1
        lda     (aRegister),y
        tax
        lda     (aRegister)
        sta     aRegister
        stx     aRegister+1
        .donext

; ************************************************************************************************
;
;;                                  Write the byte B into A
;
; ************************************************************************************************

		.bytecode  "c!"
WriteByte:
        lda     bRegister
        sta     (aRegister)
        .donext

; ************************************************************************************************
;
;;                                  Write the word B into A
;
; ************************************************************************************************

		.bytecode  "!"
WriteWord:
        phy
        lda     bRegister
        sta     (aRegister)
        lda     bRegister+1
        ldy     #1
        sta     (aRegister),y
        ply
        .donext

; ************************************************************************************************
;
;;                                  Add B into the word at A
;
; ************************************************************************************************

		.bytecode  "+!"
AddWord:
        phy
        clc
        lda     bRegister
        adc     (aRegister)
        sta     (aRegister)
        lda     bRegister+1
        ldy     #1
        adc     (aRegister),y
        sta     (aRegister),y
        ply
        .donext

        .endsection
